import UIKit

public extension UIColor {
    
    public class func darkRed() -> UIColor {
        return UIColor(red: 190/255, green: 88/255, blue: 83/255, alpha: 1)
    }
    
    public class func purpleRed() -> UIColor {
        return UIColor(red: 221/255, green: 75/255, blue: 77/255, alpha: 1)
    }
    
    public class func lightGray() -> UIColor {
        return UIColor(red: 174/255, green: 174/255, blue: 174/255, alpha: 1)
    }
    
    public class func extraLightGray() -> UIColor {
        return UIColor(red: 248/255, green: 248/255, blue: 248/255, alpha: 1)
    }
    
    public class func darkGray() -> UIColor {
        return UIColor(red: 87/255, green: 87/255, blue: 87/255, alpha: 1)
    }
}
